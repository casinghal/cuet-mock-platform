        functions.logger.info("GAT_MOCK_START", { attempt, hasCaContext: !!caContext });

        const b1raw = await callAnthropic(buildGATMockBatch(1, caContext), 6000, apiKey);
        const b1 = Array.isArray(b1raw) ? b1raw.slice(0, 10) : [];

