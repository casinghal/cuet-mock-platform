        const b5 = Array.isArray(b5raw) ? b5raw.slice(0, 10) : [];

        const b6raw = await callAnthropic(buildGATMockBatch(6, caContext), 5000, apiKey);
        const b6 = Array.isArray(b6raw) ? b6raw.slice(0, 6) : [];

